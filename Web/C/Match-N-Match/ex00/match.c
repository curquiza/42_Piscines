int		match(char *s1, char *s2)
{
	//if (*s2 == '\0' /*&& *s1 != '\0'*/)
	//	return (1);
	if (*s2 == *s1)
	{
		if (*s2 == '\0')
			return (1);
		else
			return(match(s1 + 1, s2 + 1));
	}
	if (*s2 == '*')
	{
		while (*s1 != '\0')
		{
			while(*(s2 + 1) == '*' /*&& *(s2 + 2) != '\0'*/)
				s2++;
			if (*s1 == *(s2 + 1))
				return(match(s1, s2 + 1));
			s1++;
		}
		return (0);
	}
	else if (*s2 != *s1)
		return(0);
	return (1);
}
