int		ft_putchar(char c);

void	ft_putstr(char *str)
{
	int		i;

	i = 0;
	while (*(str + i) != '\0')
	{
		ft_putchar(*(str + i));
		i++;
	}
}

int		main(int argc, char **argv)
{
	int		index;

	index = argc;
	ft_putstr(argv[0]);
	ft_putchar('\n');
	return (0);
}
